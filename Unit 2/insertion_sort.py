ls = [15, 4, 11, 9, 7, 5, 10, 13, 17, 2]

def insertion_sort (ls):      
    if len(ls) <= 1:
        return 
    for i in range(1, len(ls)):
        x = i-1
        thisOne = ls[i]
        while x >= 0 and thisOne < ls[x]:
            ls[x+1] = ls[x]
            x -= 1
        ls[x+1] = thisOne
  
insertion_sort(ls)

